                <p>If you want to submit an SBIR/STTR proposal, you must first find a relevant topic at one of the participating agencies. The SBIR/STTR programs do not accept “unsolicited proposals,” but instead require that you respond to one of their current topics, even if they are very broad like topics found at many of the agencies that make their SBIR/STTR awards as grants vs. contracts.  All of the agencies list their topics in their solicitation or Funding Opportunity Announcement (FOA). The only exception is the Department of Energy which publishes a separate topic list several weeks before it releases its FOA.</p>
                <br>
                <p>In the early days of SBIR/STTR, you had to read a paper copy of the solicitation from beginning to end to see if there were any relevant topics. Now that the solicitations are all distributed electronically, it is much faster, easier, and more productive to find topics by using search engines. You type in your key word(s), and the search engine goes through the currently open topics (or, at your option, topics that closed in the recent past) and tells you which topics contain your key word(s).  </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/UX88rMHKX_c?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>There are a variety of SBIR/STTR topic search engines to choose from. Most of the awarding agencies have search engines, and the grants.gov system can search across all agencies that make their SBIR/STTR awards as grants.  However, if you use grants.gov, be aware that it only searches agencies that make awards as grants, and the majority of SBIR/STTR awards are actually contracts rather than grants. For example, the Department of Defense, which by itself has about 40% of the entire SBIR/STTR budget, only awards contracts through its SBIR/STTR programs.</p>
                <br>
                <p>Start by using one of the search engines that looks across all SBIR/STTR agencies, because topics can appear where you least expect them. For example, if you are interested in breast cancer research and only search the National Institutes of Health (NIH) topics, then you would miss the large breast cancer research program at the Department of Defense (DoD), and not see that the National Science Foundation (NSF) has health and medical interests. </p>
                <br>
                <div class="image">
                    <img src="../media/img/6_Slide2.jpg">
                </div>
                <br>
                <p>Some of these cross-agency search engines are free, while others are only available for a charge. Of the free ones, the best are sbir.gov and zyn.com. Surprisingly, the search engines at sbir.gov and zyn.com may yield inconsistent results. The moral here is to conduct your key word search on both sbir.gov and zyn.com or you run the risk of missing relevant SBIR/STTR topics.  </p>
                <br>
                <p>We recommend that you start your search with broad and general key words, and see how many hits you get and what other terms may be contained in the full topic descriptions. This is especially important with SBIR/STTR agencies that make their awards as grants, because they may have very broad, general topics.  If you get a lot of hits, and/or the topics aren’t relevant, then get more specific in your key words on subsequent rounds of your search. Also, be sure to use synonyms in your search, as well as acronyms as the topic author may use different terms than you to describe the topic of interest.</p>
              </div>

              <!-- Page 2 -->
              <div class="page" id="two">
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/vjWsY5-dnMQ?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>When you find a topic that looks interesting, you need to determine if there is a good fit between it and your company and its technology/innovation. This is a four step process.</p>
                <br>
                <p>First, make your own honest assessment of whether your idea really fits the topic, or if you are unintentionally forcing the fit. It is not unusual for SBIR/STTR companies to do the latter; unfortunately, this typically results in a proposal that isn’t really what the agency wants, and the proposal is quickly rejected in the evaluation process. Read and reread the topic carefully, both to look for key words that suggest limits on the agency’s interests and to try to “read between the lines” to understand what they really want. As an example of those key words, the Department of Energy recently had a subtopic under “Oil & Gas” that addressed improved drilling technologies—a careful read revealed that DOE was only interested in technologies that were helpful in shale geologic conditions.  </p>
                <br>
                <p>Second, review any literature cited or referenced in the topic description. This can lead to a deeper understanding of the topic and what the agency is interested in, and what it is not.  It also can strengthen your proposal, if you reflect in your writing that you read and understand the importance and relevance of the referenced literature.</p>
                <br>
                <div class="image">
                    <img src="../media/img/6_Slide3.jpg">
                </div>
                <br>
                <p>Third, establish a dialog with the agency’s topic author (TA) to get his/her opinion on whether your innovation/technology fits the topic. This is preferably done via a phone call, although it can be done through email if that is the TA’s preference. Formulate your questions in advance, and ask them in priority order starting with the most important. Also, formulate some reasons why you think your idea fits the topic, but don’t use them to become blind to the possibility that it doesn’t. Keep the call to about 10 minutes, by briefly asking a question, then pausing so that the TA can answer it, and then repeating this for the rest of the questions on your list. Remember, you learn more by listening, rather than speaking.</p>
                <br>
                <p>Realize that some agencies, like NASA and Department of Transportation, cannot talk to you about their topics if their solicitation is open, while others (like Department of Defense and Department of Homeland Security) can only talk to you during the first few weeks after their topics are released (in contrast, you can typically talk to TAs at grant agencies like NIH, DOE and NSF at anytime). If you are in a situation where the TA can’t or won’t talk to you, you might search the agency’s list of prior awards (either on its SBIR website or on sbir.gov) to see if they previously had the same or related topic and, if so, what kind of projects they funded under it.     </p>
                <br>
                <p>Fourth, consider whether other aspects of your proposal will appeal to the agency that has the topic that interests you. The agency may be expecting you to have an electrical engineer on your team—if you don’t have one, then you will need to either add one or reconsider proposing on this topic.  Agencies are looking for SBIR/STTR projects that, if feasible and successful, will be commercialized and sold to the agency or another customer/user—if you are only looking for funding so you can conduct some basic research in your field of interest, then that is not going to appeal to the proposal reviewers at the agency.  </p>
                <br>
              </div>


