                <p>To participate in the SBIR or STTR program, an applicant must meet a number of eligibility requirements. These requirements pertain to the applicant itself, its principal investigator, and its subcontractors. Let’s discuss each of these starting with company eligibility.</p>
                <br>
                <p>Only certain types and sizes of small business may apply for and receive SBIR or STTR awards. Some mistakenly believe that universities can submit STTR proposals, when in fact those proposals must be submitted by small businesses that are collaborating with universities or other non-profit research institutions participating on the STTR project. Remember, it is the Small Business Innovation Research and Small Business Technology Transfer programs, so with both programs the small business must be the applicant as well as the responsible business and technical lead on the proposal.</p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/9vq1presI9Y?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>So what is a small business? The SBIR/STTR definition is fairly simple. It is a firm with 500 or fewer employees. It does not matter if they are full- or part-time employees, or even leased employees—they all count toward the 500 employee limit. At the low-end of the employee spectrum, the small business could also be a start-up with one employee.</p>
                <br>
                <p>The small business must be for profit, and therefore non-profit entities, that is those with a 501(c)(3) designation by the IRS are not eligible—although once again a non-profit could be a subcontractor or a consultant to the small, for profit applicant. </p>
                <br>
                <p>The small business must be primarily U.S. owned. This is defined as having at least 51% of its ownership by U.S. citizens and/or permanent resident aliens.  This requirement could also be met if the applicant is at least 51% owned by another small business with 500 or fewer employees, as long as that parent company is at least 51% owned by U.S. citizens or permanent resident aliens. When a small business is venture-backed, additional considerations come into play.  However, not wishing to over-complicate this discussion, these considerations will be discussed in the tutorials associated with the Department of Health and Human Services, as it is with HHS that questions regarding the impact of venture-backing on eligibility most often arise. </p>
                <br>
                <div class="image">
                  <img src="../media/img/2_Slide2.png">
                </div>
                <br>
                <p>Now let’s address the eligibility of the Principal Investigator, also known as the PI. Every SBIR and STTR proposal must designate a single individual who will serve as the principal investigator on the proposed project. The PI has overall responsibility for the project, and therefore must be credible in terms of his/her education, work and project management experience. </p>
                <br>
                <p>The principal investigator has to meet certain eligibility requirements on an  SBIR/STTR proposal. For SBIR projects, the PI must be “primarily employed” by the applicant small business during the SBIR award period. The agencies don’t agree on all the specifics as to what “primarily employed” means, but all do agree that this means the PI cannot be full time employed elsewhere during the SBIR award period. Some agencies then go on to add additional stipulations, such as the PI cannot work more than 19.6 hours elsewhere, or has to devote a minimum number of hours to the SBIR program to be confirmed by the hours shown for the PI on the budget form. A careful read of the agency’s solicitation will help ensure that you stay in compliance with the eligibility requirements for the PI.  </p>
              </div>

              <!-- Page 2 -->
              <div class="page" id="two">
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/szDwL_g8X40?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>The rules for STTR are a little different at 4 of the 5 participating agencies.  Remember there are only 5 agencies that have STTR programs. These are the Department of Defense, Department of Energy, the National Aeronautics and Space Administration, the National Institutes of Health and the National Science Foundation. At the National Science Foundation, the rules on an STTR PI’s eligibility are the same as they are on an SBIR project. At the other four agencies, the PI can be primarily employed by either the small business applicant or the non-profit organization with which the small firm is collaborating on the STTR project. Similar definitions of “primarily employed” apply to an STTR as they do to an SBIR. So, once again be sure to carefully read the requirements in the solicitation of the agency to which you are applying.   </p>
                <br>
                <p>Relatively few opportunities exist in the SBIR/STTR programs to designate “Co-PI’s” in which the principal investigator role is shared by 2 or more individuals. The National Institutes of Health allows this on SBIR and STTR projects that require a “team science” approach, and refer to them as “multiple PIs”. However, special requirements exist for this structure and a Leadership Plan must be submitted. NSF uses the term Co-PI with respect to NSF STTR proposals, all of which must involve a subawardee research institution. The lead from that institution is referred to as a co-PI and companies are instructed to designate a “Co-PI from that institution listed on the cover page and on the sub-budget.” This is a naming convention. Once again, we advise consulting the solicitation for agency-specific guidance on Co-PIs.  You can always contact the agency’s SBIR/STTR program manager to clarify what is allowed or required.</p>
                <br>
                <p>Now let’s examine Subcontractor eligibility. On a Phase I SBIR project, up to 33% of the research can be subcontracted to another entity.  That entity can be anything from a non-profit research institution such as a university or Federal laboratory, to an individual consultant, or a large prime contractor.</p>
                <br>
                <p>On STTR projects, either Phase I or II, at least 30% of the research must be performed by a non-profit entity referred to as the Research Institution or RI. The RI typically is a university (public or private) or a Federal Laboratory (provided it qualifies as a Federally Funded Research and Development Center or FFRDC). The applicant small business must perform at least 40% of the R&D on an STTR project, so the RI could do as much as 60% of the research. A single RI must provide at least 30% of the research, while either the same entity or other subcontractors and consultants could contribute additional amounts so that the total sub-awards do not exceed 60%. When it comes to measuring these percentages, be sure to carefully read how the agency to which you are applying wants it measured.  Agency differences are discussed in the individual agency section of the tutorial</p>
                <br>
                <div class="image">
                  <img src="../media/img/2_Slide4.png">
                </div>
                <br>
                <p>We said earlier that to be eligible for SBIR/STTR funding, the applicant small business must be primarily U.S. owned. A reasonable question, then, is whether this same requirement applies to the subcontractors, consultants, and other sub-awardees on an SBIR/STTR project. The answer differs between SBIR and STTR.</p>
                <br>
                <p>In SBIR, the sub-awardee does not have to be located in the United States. However, all of the R&D work performed by the sub-awardee must be done in the US, so an SBIR application involving a foreign sub-awardee needs to address how that person or entity will be able to fulfill the “all R&D done in the US” requirement. There also may be agency-specific limits here; for example, the Dept of Defense may have concerns about, and limits on, work being done outside of the US or by non-US citizens.</p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/PBaqUl51wzY?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>In the STTR program, the nonprofit entity that is serving as the Research Institution must do at least 30% of the R&D on the project. That RI must be located within the US. The only opportunity for non-US involvement in an STTR project, then, will occur when the US owned small business submits an STTR proposal involving a US based non-profit RI, and also involves another sub-awardee on the project. On an STTR, the small business applicant must do 40% of the R&D, and the RI must do 30%, leaving another 30% that could be done by the applicant, the research institution, or another sub-awardee. If it is the latter, then a foreign sub-awardee could be included, again with the stipulation that it must perform all of its R&D on the STTR project while in the US.  Please note that this “STTR foreign sub-awardee” rule differs by agency, and some will not allow any part of an STTR project to be performed by non-US sub-awardees.  </p>
                <br>
                <p>Be sure that you review and understand the eligibility requirements before you begin to invest time in developing your proposal to assure that your firm is eligible and that you comply with the guidelines of the agency to which you will be applying. Remember that the requirements may be different in SBIR and STTR solicitations. Don’t hesitate to contact the agency SBIR/STTR program manager if you are unclear on the requirements. And do not ignore the eligibility requirements as the Federal government has chosen to emphasize this as an area of greater scrutiny.</p>
                <br>
