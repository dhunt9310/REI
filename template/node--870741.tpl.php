              <div class="page" id="one">
                <p>Every SBIR or STTR program takes on the unique characteristics of the agency in which it resides. The National Science Foundation’s SBIR/STTR program is no exception.  NSF supports fundamental research and education across all fields of science and engineering.  With an annual budget of $7.6 billion, NSF is the funding source for approximately 24% of all federally supported basic research conducted by America’s colleges and universities. </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/-TnFxkrSqj8?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>The NSF SBIR /STTR program resides within the Division of Industrial Innovation and Partnerships or IIP. Its vision is to drive the expansion of the nation’s innovation capacity by stimulating partnerships. It looks to enhance economic competitiveness by catalyzing the transformation of discovery into societal benefits.  This vision shapes the implementation of the NSF SBIR/STTR program which funds game changing, disruptive technologies. NSF’s solicitations are extremely broad. You identify the problem or opportunity, you propose the technological solution and devise your business strategy. One of the hallmarks of the NSF program is its emphasis on commercialization. NSF was the first agency to have an SBIR program and an emphasis on commercialization has always been its cornerstone.</p>
                <br>
                <p>With an annual budget of approximately $170M, the NSF SBIR/STTR program funds roughly 400 projects a year.  The agency’s sweet spot is early stage, high risk technology at the pre-seed level. The goal is to conduct research and development that overcomes significant technical hurdles in order to prove the feasibility of a new product, process, or service. Applicants are expected to think deeply about commercialization and develop solutions that could create significant commercial success and/or societal impact.</p>
                <br>
                <p>The NSF program is unique in many ways. The Program Directors have deep technical and business expertise and directly provide guidance and mentorship to small businesses. NSF believes strongly in the benefits of connecting with others and facilitates that connection. The program funds highly technical R&D in broad technology areas. These include:
                  <ul>
                    <li>Education Technologies and Applications (EA)</li>
                    <li>Information Technologies (IT)</li>
                    <li>Internet of Things (I)</li>
                    <li>Semiconductors (S) and Photonic (PH) Devices and Materials</li>
                    <li>Electronic hardware, Robotics and Wireless Technologies (EW)</li>
                    <li>Advanced Manufacturing and Nanotechnology (MN)</li>
                    <li>Advanced Materials and Instrumentation (MI)</li>
                    <li>Chemical and Environmental Technologies (CT)</li>
                    <li>Biological Technologies (BT)</li>
                    <li>Smart health (SH) and Biomedical (BM) Technologies</li>
                  </ul>
                </p>
              </div>

              <!-- Page 2 -->
              <div class="page" id="two">
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/OMukUK4yqec?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>In trying to determine if the NSF SBIR/STTR program is a good opportunity for you and your company– it is recommended that you consider two things: (1) Is this a genuine innovation – an approach that is highly disruptive and technically risky. That is what NSF is looking for – not evolutionary technologies, but those that are revolutionary in nature; (2) The second thing to consider is if you are organized to aggressively validate a market need and pursue commercialization. Again, that is expected at NSF. Potential proposers can solicit Pre-Submission feedback from the appropriate Program Director by preparing and then sending him or her a 1-2 page executive summary by e-mail. Feedback from the Program Director will help the potential applicant  gauge whether a project meets the program’s intellectual merit and commercial impact criteria. These criteria are clearly described in the solicitation </p>
                <br>
                <p>NSF is start-up friendly. Eighty-five percent of awardees have 10 or fewer employees. In fact several dozen Phase I awardees each year incorporate based on receiving a Phase I NSF award. Approximately seventy-five percent of awardees have never had a prior SBIR/STTR Phase II award from any agency. Seventy percent of awardee companies were incorporated within the past 5 years. </p>
                <br>
                <p>NSF provides grants, not contracts. Phase I awards are up to $225,000 to test feasibility and conduct proof-of-concept research over a 6-12 month period. Phase II awards are up to $750,000 and last 24 month. During Phase II the company is expected to engage in  prototype development, scale up and testing. Phase II applicants, by the way must have received a Phase I NSF SBIR/STTR award.</p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/hhOex2Aaato?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>The National Science Foundation provides a wide variety of Supplements designed to encourage partnerships and commercialization. Phase IB awards encourage third party investment during Phase I by providing a 1:2 match with NSF providing up to $30K.  State funds are considered a viable source of matching during Phase I and Phase II. At the Phase II level, there is a Phase IIB Supplement. At this stage of development NSF will provide a 1:2 match up to $500K – meaning that if you secure a $1M investment, NSF will provide an additional $500K. Another Phase II supplement is referred to as the TECP which stands for Technology Enhancement for Commercial Partnerships. This program provides a supplement to SBIR or STTR awardees in order to pave the way for partnerships between strategic corporate partners and investors. The intent of this supplement is provide funding for additional research that goes beyond the Phase II project’s objectives to meet the technical specifications or additional proof-of-concept requirements of the potential commercialization partner.</p>
                <br>
                <p>Apart from these supplements designed to encourage commercialization, there are others aimed at supplementing your team. These include Educational partnerships with high school students,  teachers, undergraduate students, and US veterans, as well as Institutional Partnerships. Be sure to explore these.</p>
                <br>
                <p>Another program developed by the National Science Foundation is the I-Corps program, originally designed to capitalize on previously NSF-funded basic research and nurture a national innovation ecosystem by helping discoveries from fundamental research to become new technologies that can benefit society. NSF now offers an I-Corps Lite program for SBIR/STTR awardees.</p>
                <br>
                <p>To find out more about the NSF SBIR/STTR program be sure to visit the Agencies website and listen to the tutorial regarding the NSF solicitation process. </p>
              </div>


