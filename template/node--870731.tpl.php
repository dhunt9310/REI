              <div class="page" id="one">
                <p>Small companies that are successful in winning SBIR and STTR awards tend to have open lines of communication between themselves and representatives of the awarding agencies. One agency representative with whom communication is important is the Topic Author or TA. Depending on the agency, different titles are used for this person including topic author, topic manager, technical point of contact or OA COR, to name a few.  Basically you can think of this person as the individual who in all likelihood has written the topic and with whom you, the small business applicant can have a conversation if you have questions regarding the topic itself.  I can think of nothing more important than talking with the topic author when you have the opportunity to do so.</p>
                <br>
                <div class="image">
                    <img src="../media/img/7_Slide2.png">
                </div>
                <br>
                <p>Remember that some agencies, like NASA and Department of Transportation, cannot talk to you about their topics if their solicitation is open, while others such as the Department of Defense and the Department of Homeland Security can only talk to you during the Pre-Release period which is the first few weeks after their topics are released. In contrast, you can typically talk to topic authors at granting agencies such as the Department of Health and Human Services, the Department of Energy or the National Science Foundation at anytime.</p>
                <br>
                <p>The primary reason to speak with the topic author is to glean additional insights regarding the topic. Agencies are often constrained in how much detail they can put into the solicitation or a funding opportunity announcement (FOA). Therefore some topics are broad and may leave lingering questions in the potential applicants mind. Another reason that a topic may be broad is because the agency wants a diversity of responses - so a conversation will be very helpful in gaining the needed clarity. </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/QO98RG3hsbE?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>Another reason to have a conversation is to help you make a decision which topic to respond to when it appears that there are multiple topics that seem appropriate. The topic author often understands the true intent or priorities of one topic versus another and may be able to share that information with you, as well as offer some advice on common mistakes that applicants make. This conversation is also an opportunity to ask the TA whether special agency initiatives, such as Fast Track, are appropriate for your company and your proposal. </p>
                <br>
                <p>Communication is a two way street. This initial call also provides you with the opportunity to introduce yourself and your company to the topic author.  Decisions about you and your proposal will not be made during this conversation, but a favorable interaction can help “break the ice” that otherwise might exist between you as a newcomer (to SBIR/STTR and/or this agency) and the TA.</p>
              </div>

              <!-- Page 2 -->
              <div class="page" id="two">
                <p>Many newcomers to the SBIR/STTR processes are reluctant to call a topic author, as they are uncertain what they can and cannot ask. With some agencies such as the Department of Energy, the agency has been fairly specific about what it is looking for, so your questions should be focused on gaining a better understanding of the agency’s need and the “fit” between their needs and your thoughts regarding a solution. Your goal for the phone conversation is to end the call with a clear understanding of what the agency wants and equally important, what the TA doesn’t want and/or is not interested in.  In this way you can assess whether your innovation will satisfy an agency’s need. The TA can be helpful in providing insights and information that will allow you to make the determination as to whether or not you can deliver what the agency truly wants. Paraphrasing one agency representative, “give us what we asked for, not what you think we need.”</p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/FhGWqxFOJ1o?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>You are encouraged to set up a conversation with the topic author via an introductory email. Indicate you are interested in submitting a proposal on the topic for which the TA is responsible, but that you have some questions. Ask if they would be willing to speak with you for a brief period of time (10 to 15 minutes) at their convenience in the next few days. It is preferable to ask your questions by phone, as this leads to a richer dialog and makes it easier to ask clarifying questions - but realize the TA may prefer to correspond via email. Also, be mindful of the agency variations and read the solicitations carefully to determine if and when you can speak directly with the topic author.</p>
                <br>
                <p>Develop questions in advance of the conversation with the topic author. Keep your questions brief and to the point, so that there is sufficient time for the Topic author to respond. Keep in mind that you learn when the topic author speaks, not when you do.  You may also want to briefly explain what you are planning to propose and seek the TA’s guidance on whether or not this is a fit with the topic. However, leave this to the end of your conversation, until you’ve asked sufficient questions to know whether your innovation is consistent with the intent of the topic.  </p>
                <br>
                <div class="image">
                    <img src="../media/img/7_Slide3.png">
                </div>
                <br>
                <p>Don’t ask questions that are readily answered by the agency’s solicitation and be sure to review the background information cited in the solicitation before you have this conversation. You will be surprised by how much you can learn during a brief conversation with the topic author if you are disciplined and ask brief questions and pause to allow the TA to respond.</p>
                <br>
                <p>A final reminder, some agencies have limitations on- if and when you can speak with topic authors.  It is recommended that you have your conversation as soon as possible after the topics are released as it is important for you to determine quickly if a particular topic represents an opportunity. Having this conversation early allows maximum time for you to prepare a responsive proposal.</p>
                <br>
              </div>


