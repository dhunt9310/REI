      <h1>Defense Threat Reduction Agency (DTRA)</h1>
      <br>
      <p>The Defense Threat Reduction Agency or DTRA is the youngest agency in the Department of Defense. Formed in 1998 the mission of DTRA is to keep weapons of mass destruction or WMD out of the hands of terrorists and other enemies by locking down, monitoring, and destroying weapons and weapons related materials. DTRA is a combat support agency meaning that it provides support to the US military during combat operations. With its headquarters in Fort Belvoir, Virginia DTRA’s vigilance is constant, 24 hours a day, 7 days a week. </p>
      <br>
      <p>In order to anticipate future threats, DTRA manages a research and development portfolio through a number of departments dedicated to Basic and Applied Science, Chemical/ Biological Technology, Counter WMD Technology, Nuclear Technologies and The Defense Threat Reduction Information  Analysis Center (DTRIAC).</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/nefJs50yXNE?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>The Basic and Applied Science Department has two interlinked goals:  First to stimulate the development of innovative solutions to enable revolutionary change for achieving threat reduction and second, promoting the development of the next generation  science and technology workforce for combating WMD. To this end, basic research grants of up to $350,000 per year for up to 5 years are provided to accredited degree-granting colleges, universities, and academic institutions. This is a non- SBIR initiative.</p>
      <br>
      <p>The Counter WMD Technologies Department develops and demonstrates innovative technologies to actively counter the full spectrum of Chemical, Biological, Radiological, Nuclear and Explosives or CBRNE threats; while the nuclear technologies department seeks to enhance the safety, security, survivability, and performance of US assets and facilities in a nuclear environment.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/jvURk-qUKqM?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>The DTRA SBIR program is fairly small, with an annual budget of less than $10 million.  The topics in the DTRA solicitation reflect current strategic priorities where small businesses are believed to have capabilities to address challenging technical issues. DTRA Phase I SBIR awards are for up to $150,000 and need to be accomplished within seven months. For more information on the DTRA SBIR program, potential applicants are encouraged to contact Mr. Mark Flohr, the SBIR Program Manager. </p>
