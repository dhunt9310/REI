      <h1>USSOCOM</h1>
      <br>
      The United States Special Operations Command or USSOCOM has its headquarters at MacDill Air Force Base in Tampa, Florida. It is the only Unified Combatant Command legislated into being by Congress and is charged with overseeing the Special Operations Component Commands of the Army, Marine Corps, Navy and Air Force. The special operations forces or SOF conduct covert and clandestine missions such as special reconnaissance, counter-terrorism, unconventional warfare and many other special operations. 
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/Nqvp-Q_PPZQ?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>Let’s talk about the USSOCOM SBIR program, also referred to as SBIR program. What does USSOCOM want to get out of it?  What makes USSOCOM different than other SBIR agencies? USSOCOM is a combatant command with global responsibilities. A special operator needs to be equipped and trained to work in every environment within the world  - in the air, on the land, on the surface of the sea and below the sea. SOCOM has component organizations of its own that mirror the services  - so you will hear mention of the United States Army Special Operations Command [USASOC],  the Air Force Special Operations Command [AFSOC], the Marines [MARSOC]. The Navy Component is referred to as the Naval Special Operations Warfare Command.</p>
      <br>
      <p>The USSOCOM SBIR program does not have science projects. There’s no research for the sake of research. A project isn’t funded because a certain individual may find it interesting. USSOCOM focuses solely on the needs of the warfighter. It looks for leading edge technology. The logo for USSOCOM is the spear and this symbol is incorporated in the SBIR logo as the letter I. The special operators are at the tip of that spear and they require leading edge technology. When SOCOM sends special operators into harms way, it wants them to have a technological advantage over their adversaries. As the saying goes, a level playing field is for sports, not war.</p>
      <br>
      <p>You may be concerned that developing technology for a limited group of warfighters may limit commercialization, but don’t worry – the US conventional forces, carefully watch what is created for the special operator and tend to adopt the same technology for the Army, the Air Force, the Navy or Marines. They know that USSOCOM is always looking for that leading edge technology that will give them an advantage over their adversaries.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/yZjoEct5l-c?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>USSOCOM participates only in the first solicitation cycle of the Fiscal year and does not have an STTR program. USSOCOM expects that companies that receive SBIR funding from SOCOM will commercialize the technology and be a continuing source of supply. One of the practices that SOCOM implements frequently is cross-agency Phase II awards.  To provide the SOF warfighter with the best technology as quickly as possible, Phase I SBIR awardees originally funded by another DoD component or even another Agency can request Phase II SBIR funding from USSOCOM. To determine if there is a good fit for a Cross-Agency award from SOCOM, Phase I SBIR awardees should reach out to USSOCOM’s Technical Industrial Liaison Office (TILO) which will require a two page whitepaper.  Approximately a quarter of those that submit a request from TILO – will be asked to provide more information.</p>
      <br>
      <p>USSOCOM wants to see leading edge technologies get to Phase III  and benefit the special operator. In order to be successful in Phase III, you must understand the requirements of the Program Executive Office or PEO that is funding your SBIR award. It’s important that you deliver your work on time and on budget – as USSOCOM wants reliable suppliers. </p>
      <br>
      <p>SOCOM will help you promote your product through events such as the Technical Experimentation or TE events. These are held three times a year and provide a unique opportunity for technology developers to interact with the SOF community in a collaborative environment. Another opportunity to gain more exposure is through attendance at the Special Operations Forces Industry Conference or SOFIC, held in May each year in Tampa, FL. This is where the leaders of SOCOM clarify what they need from industry. USSOCOM also surveys allied interests.  ITAR does not prevent you from selling to allies – it means that you need to know who are allies are and have an export license.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/PPxAYqyoA1M?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>In closing, let me introduce the SOCOM PEOs. The Program Executive Offices are acquisition organizations that reside within the Special Operations Forces Acquisition, Technology, and Logistics Center or SOF AT&L. Each PEO has a military or civilian official at the helm who has responsibility for directing several Major Defense Acquisition Programs (MDAPs) and non-major system acquisition programs.  </p>
      <br>
      <p>PEO C4 is responsible for Command, Control, Communications and Computers. It includes enterprise networks transport, tactical coms, as well as Military Information Support Operations (MISO). </p>
      <br>
      <p>PEO Fixed Wing is responsible for Special operations manned and unmanned fixed wing airpower capabilities .</p>
      <br>
      <p>PEO Maritime’s vision is to build unmatched affordable surface and undersea platforms that deliver SOF safely and undetected anytime and anywhere on the globe.</p>
      <br>
      <p>PEO Rotary Wing is responsible for providing the SOF community with the advanced vertical lift capability available to the US military. </p>
      <br>
      <p>SOF-Warrior provides enhanced lethality, mobility, and survivability while performing ground SOF activities across the spectrum of conflict in all environment conditions.</p>
      <br>
      <p>The Program Executive Office for Special Reconnaissance, Surveillance, and Exploitation (PEO-SRSE) is responsible for the acquisition, fielding, and sustainment of intelligence systems for Special Operations Forces (SOF). The systems contribute directly to USSOCOM priorities to deter, disrupt, and defeat terrorist threats and sustain and modernize the force in persistent intelligence surveillance and reconnaissance.</p>
      <br>
      <p>And finally, PEO Special Operations Forces Support Agency of PEO-SOFSA supports the SOF community through the execution of contracting, financial management, and operations oversight to ensure dedicated, responsive, and cost-effective contractor logistics support services.</p>
      <br>
      <p>To learn more about the USSOCOM SBIR program please look at the components website. Also be sure to look at TILO, the Technical Industrial Liaison Office and learn more about the USSOCOM PEOs. </p>
