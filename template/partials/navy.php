      <h1>Navy</h1>
      <br>
      <p>The primary goal of the Department of the Navy’s SBIR and STTR programs is to use small business to develop innovative technologies that meet a broad range of Navy and Marine Corps needs. The ultimate goal is to insert SBIR/STTR technologies and products into platforms and systems that help the Department of the Navy, also referred to as DON, to achieve its mission.  The Navy’s process is acquisition-driven with strong technology pull from the various Systems Commands, referred to as SYSCOMs. Given the size and diversity of the Navy, there are multiple SBIR program managers associated with the various SYSCOMs.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/kgA2jS5MkrE?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>Navy topics address a broad range of needs associated with ground, sea, and air platforms; as well as Command, Control, Communications, Computers & Intelligence  or C4I; and weapons technologies to name a few.  The accompanying graphic shows the number of topics by research category from a 2015 solicitation. Please note that research in cyber security, engineered resilient systems, Counter-IEDs, autonomy, and energy and power technologies were all funded during this period. However, these topics were included in other research categories based on prior taxonomies. </p>
      <br>
      <p>The metrics chart shown  in the video illustrates the Department of the Navy’s funding over the past 4 years. This chart also shows changes in the number of topics over time as well as changes in the number of Phase I awards made and new Phase II awards. When looking at the last year in the chart – please note that this is not a complete data set – as this changes over time. .With respect to STTR, the budget is beginning to increase. If we look at the number of Phase I proposals received they remain high, averaging about 2,500 per year. Approximately 15% of Phase I applicants receive an awarded contract.  The average time to make a Phase I award is a little over 4 months and the Navy has a good number of SBIR awards transitioning to Phase III.</p>
      <br>
      <p>What does the life of a Topic look like? How do things transition to Phase III. If  we use 170 topics as the standard,  we would anticipate receiving a little over 2,800 proposals. Multiple awards would be made within each topic area and on average 482 awards would be made – almost three awards per topic.  Once awarded approximately half of those that receive a Phase I award would receive a Phase II award [254]. Phase III is the ultimate goal – and is achieved when a program of record or the private sector provides non-SBIR/STTR funding to continue the maturation of the technology that was initially funded by the SBIR/STTR programs. Of the initial 482 Phase I awards made, approximately 137 or 28% would receive Phase III funding.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/g3U0fEHbIXM?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>How is funding provided? The Navy’s Phase I award is comprised of a Base and Option. Phase I awards are firm fixed price contracts with a maximum dollar amount not to exceed $150K. The award of an Option is not guaranteed and depends on performance against various milestones. Phase II is comprised of a Base and two Options. This is a Cost Plus Fixed Fee contract not to exceed $1.5M for either the SBIR or STTR award. For prototype testing and evaluation, the Navy does utilize Subsequent Phase IIs where a cost match is required. As noted previously, the ultimate goal is transition to Phase III when technology pull is evidenced by providing additional funds from a non- SBIR/STTR source. This whole process commonly takes up to 6-9 years, depending on the technology and the application. The total amount of funding that a small business could receive to develop a technology from one topic,  utilizing all of these mechanisms is up to $3.15M, but non-SBIR/STTR sources of funding may be unlimited.</p>
      <br>
      <p>The Navy makes use of the Commercialization Readiness Program (CRP) which was created by Congress in FY06.  The Department of the Navy sets aside approximately 20% of its annual SBIR funding for the CRP, the goal of which is to accelerate the transition of SBIR/STTR technology to Phase III. The selection process is vigorous and requires validation of the Department of the Navy’s requirements, as well as  validation of the maturity of the technology and the firms overall capabilities. A commitment of non-SBIR/STTR matching funds is generally required. This program has invested over $510M in 276 projects since FY06 and has reported a total return on investments of $853 million with $327M coming from direct government Phase III funding and $526M being reported from non-government funding reported by the small businesses themselves.</p>
      <br>
      <p>In the accompanying tools, you will find the contact information for Navy SBIR/STTR program managers. Also be sure to explore the navy sbir search website and the Navy’s website to find out more about the program, topics, and success stories.</p>
