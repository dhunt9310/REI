      <h1>Army</h1>
      <br>
      <p>The mission of the Army SBIR program is to provide, small high-tech businesses with the opportunity to propose innovative research and development solutions in response to critical Army needs. </p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/l-IObWqFezQ?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>The Army SBIR Life Cycle begins with input from the field which provides an understanding of the Soldier’s Critical Needs based on information gathered through various mechanisms such as War Fighter Outcomes (WFOs). These needs are reflected in Technology Roadmaps and considered when generating SBIR and STTR topics. Army topics are written by scientists and engineers from the Army Labs, R&D Centers, and Program Executive Offices, also known as PEOs.</p>
      <br>
      <p>The Army typically receives between two and three thousand SBIR Phase I proposals a year and funds between 10 and 13% of them. The Phase I awards are comprised of a base, not to exceed $100,000 and six months of effort, and a Phase I Option not to exceed $50,000 and four months of effort. Phase II typically lasts for 2 years and has a ceiling of $1 million. Phase II provides the opportunity to validate and mature the topic solution. Roughly fifty percent of Phase I SBIR awardees receive a Phase II award. The Army also has a Phase II Enhancement program which enables it to provide an additional $500,000 if a company can match these additional SBIR funds with non-SBIR funding from a DoD acquisition program or from the private sector. The goal for good SBIR/STTR projects is to transition to Phase III, assuming that the need persists and the solution is cost effective. The hallmark of Phase III is that the company will continue the maturation of the technology with non-SBIR/STTR funding from either the government or the private sector. The end result of sustained and collaborative efforts during Phase III is that solutions needed by the war fighter are provided.</p>
      <br>
      <p>Like other services, the Army’s budget for SBIR and STTR awards has decreased over the last 10 years - going from $243 million in 2007 to $151 million in 2015. Due to this decrease, the number of topics, as well as the number of Phase I and Phase II awards have been reduced. However, in 2016 this trend has been reversed and the budget has begun to recover. The participating Army organizations that provide topics include a dozen PEOs, as well as numerous Command and Research centers. The term PEO by the way, can be used to refer to the program executive officer, that is the main stakeholder responsible for cost, schedule and performance in a DoD acquisition program, and for the entity or program managed, also referred to as a PEO – such as PEO AMMO or PEO Soldier.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/RKJUlnrCmtU?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>To help you learn more about the Army organizations which sponsor topics, we have included a set of short videos prepared by those organizations within the Army which fund the greatest number of SBIR topics including the Communications, Electronics Research, Development, and Engineering Center or CERDEC which funded 16 of the FY 2015 topics, as well as the Army Research Labs or ARL which funded 14 topics, and the Medical Research and Materiel Command or MRMC which funded 11 topics. To view these videos, please navigate to the Tools section, and look under Customers.</p>
      <br>
    </div>
    <div class="page" id="two">
      <p>The accompanying graphic provides an overview of some of the key technology areas of interest to the Army and includes Advanced Materials and Manufacturing, Microelectronics and Photonics, Sensors and information processing, simulation and modeling, and Engineering sciences to name a few. Eighty to 90% of the Army’s SBIR topics are released during the first solicitation cycle for each fiscal year. The Army STTR program only participates in the first cycle.</p>
      <br>
      <p>The ultimate goal for good SBIR and STTR projects is to transition to Phase III, assuming they are cost effective and that the need persists. Phase III allows for the continued maturation of the technology with non-SBIR funding from either the government or the private sector.  Please be sure to review the success stories on the Army’s SBIR/STTR website in order to gain insight into how others have been successful in the transition process. A Commercialization Brochure is produced annually and can be downloaded from the Army SBIR/STTR website.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/XUsRmDCgFEk?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>To facilitate Phase III Transition, the Army SBIR program office has developed a unique network of Technical Assistance Advocates (TAAs) that are strategically placed within various Army organizations. Each TAA has a technology focus. They provide expert advice and analysis to SBIR and STTR awardees to improve technical decisions, solve problems, minimize risk, and assist with commercializing their technologies. The list of TAAs is also provided in the tools section of this tutorial.  Army SBIR and/or STTR awardees are strongly encouraged to develop relationships with relevant prime contractors and PEOs as potential transition partners. We encourage you to review the Defense Acquisition Tutorial to learn more about the defense acquisition process.</p>
      <br>
