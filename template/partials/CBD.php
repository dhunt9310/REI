      <h1>Chemical and Biological Defense (CBD)</h1>
      <br>
      <p>The Chemical and Biological Defense (CBD) program was established by the Department of Defense (DoD) in 1994 to provide state-of-the-art defense capabilities to allow U.S. military forces to operate and successfully complete missions in chemical and biological warfare environments. </p>
      <br>
      <p>The overall objective of the CBD Small Business Innovation Research (SBIR) program is to elicit innovative solutions from the small business community that address chemical and biological defense technology gaps confronting DoD and to include technologies that will also have high commercialization potential in the private sector. Technologies developed under the SBIR program have the potential to transition to the Joint Program Executive Office for Chemical and Biological Defense (JPEO-CBD) if the appropriate level of technology maturity has been demonstrated. </p>
      <br>
      <p>The CBD SBIR program is one of the 12 components within DoD and should not be confused with DTRA or the Defense Threat Reduction Agency. Although the Science and Technology portfolio for the Joint Chemical and Biological Defense Program is managed at DTRA, the CBD SBIR program  and the DTRA SBIR program are separate programs, each with a separate budget and a separate program manager.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/LMN-uTYQ7RI?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>The CBD SBIR Program targets technology efforts that maximize a strong defensive posture in a biological or chemical environment using passive and active means as deterrents. These technologies include chemical and biological detection for both point and stand-off capabilities; individual and collective protection; hazard mitigation (or decontamination); information systems technology to include, but not limited to, modeling, simulation and operational effects & mitigation; medical pre-treatments (such as vaccine development and delivery); medical diagnostics & disease surveillance; and medical therapeutics (such as chemical countermeasures and biological countermeasures).</p>
      <br>
      <p>CBD participates in both the SBIR program and the STTR program. The programs fund a large and diverse array of topic areas and technical disciplines including: detectors and sensors, medical pre-treatment, therapeutics, and diagnostics; decontamination and protection; bioinformatics and data analysis systems; solder support, health and performance; proteomics, molecular biology, drug development, and many more.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/v9q1bM4691w?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>The CBD SBIR Program uses a base and an option for Phase I contract awards. The maximum dollar value for the base is $100,000 for up to 6 months of work, while the Phase I Option is $50,000 for three months. CBD provides three opportunities annually for Phase I awardees to submit a Phase II proposal and encourages Phase I awardees to defer Phase II proposal submission until at least the fifth month of the Phase I Period of Performance. The total SBIR funding available for Phase II is $1 million. Approximately one in ten Phase I proposals are selected for contract award. Approximately two proposals are selected for each topic. Approximately 50% of Phase I awardees receive a Phase II contract. The CBD STTR Program only employs a base period and no option period is offered. The maximum dollar value for the Phase I base is $150,000 for up to 6 months.</p>
      <br>
      <p>For more information on the CBD SBIR & STTR Programs, please visit the Chemical and Biological Defense website at www.cbdsbir.com</p>
