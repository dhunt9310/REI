      <h1>Missile Defense Agency (MDA)</h1>
      <br>
      <p>The Missile Defense Agency or MDA is a research, development, and acquisition agency within the Department of Defense. It’s mission is to develop and field integrated, layered, ballistic missile defense systems (BMDS) to defend the United States, its deployed forces, allies, and friends against all ranges of enemy ballistic missiles in all phases of flight.  MDA doesn’t deal with cruise missiles, air defense or ship defense – its singular focus is ballistic missiles. </p>
      <br>
      <p>A ballistic missile has the objective of delivering one or more warheads to a predetermined target.  Since 2008, the threat has been increasing. North Korea is developing the KN-08 intercontinental ballistic missile or ICBM and Iran may currently be technically capable of flight testing an ICBM. To defend against such missiles,  MDA has assets in space, in the oceans, on land and with deployed forces. It has sensors and weapon systems around the world. Let’s look more closely at MDA’s mission.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/W_rbUYu83cg?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>To defend the United States against ballistic missiles DoD must have a comprehensive suite of sensors including (1) Space tracking and surveillance systems, (2) Forward-based radar, (3) Sea-based X-Band Radar, (4) Early Warning radar , and AEGIS BMD SPY-I Radar. The trajectory of a ballistic missile has three parts: powered flight, free-flight, and the re-entry phase. The range of the missiles can vary and are classified as short, medium, intermediate, intercontinental and submarine launched ballistic missiles.  These missiles can be launched from mobile systems such as aircraft, ships, and submarines as well as from fixed sites.  In order to enhance security, a layered defense system is used which means that they security system has multiple tools and policies to safeguard the systems against unauthorized access.</p>
      <br>
      <p>MDA has the fourth largest SBIR/STTR program within DoD and participates in the second or third solicitation cycle of each fiscal year. MDA seeks revolutionary technologies that can be utilized as part of the Ballistic Missile Defense System.  Sample topics of interest include satellite technology, radar and electro-optic sensors especially for seekers, any form of missile technology including light weighting materials and propulsion. What ties this all together is command and control systems – data fusion and asset management for example. </p>
      <br>
      <p>In 2015 the MDA budget for SBIR awards was $79M and for STTR was $11M. MDA Phase I SBIR awards consist of a base not to exceed $100,000 and a Phase 1 option not to exceed $40,000. For MDA STTR awards, the base is $100,000 also, but the Phase I option is $25,000. The time frame for the base and the Option in both programs is 6 months each. Phase II SBIR awards are for up to $1,000,000. To assist with transition to Phase III, MDA has a Commercialization and Transition Program Office. This group assists firms to develop relationships with MDA elements, prime contractors, and other qualified advisors with the intention of inserting needed technology into the Ballistic Missile Defense System. </p>
      <br>
      <p>The MDA SBIR and STTR program office is located within the Advanced Technology Division at Redstone Arsenal in Alabama. This Division also has other contracting vehicles of potential interest to small business and Universities – specifically Broad Agency Announcements or BAAs. The Missile Defense Science and Technology Advanced Research (MSTAR) BAA is continuously open for proposals from universities. Another BAA that is open to both universities and commercial vendors is the Advanced Technology Innovation Broad Agency Announcement or ATI BAA. This too is continuously open.  To learn more about the MDA SBIR/STTR program or the BAAs, please consult the contacts provided.</p>
