      <h1>AIR FORCE</h1>
      <br>
      <p>The mission of the Air Force SBIR/STTR program office is to “increase the use of small businesses as a valued source of skills for innovative, cost effective, customer-focused solutions to warfighter requirements and Air Force missions both now and in the future.” The US Air Force has the largest SBIR and STTR budgets within the Department of Defense and has implemented a number of unique features to facilitate technology transition.  The Air Force typically releases most topics during the first solicitation cycle of the fiscal year but also uses the second and third solicitation on an as needed basis. Unlike other components that divide Phase I into a base and an Option, the Air Force typically provides the Phase I awards in their entirety up to $150,000 for a Phase I SBIR or STTR contract. The work on an SBIR award is to be accomplished in 6 to 10 months; while with an STTR award, the work is typically accomplished in 6-12 months.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/IJzxUHwI1Gw?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>To facilitate transition to Phase III, the Air Force hosts a series of Small Business Industry Days (SBIDs) that focus on small business product capabilities, contract opportunities and technology development prospects by matching stakeholders from Program Executive Offices, large business, and small business communities. The SBIDs are designed to increase small business participation and awareness in the Air Force Supply Chain. Another tool used by the Air Force SBIR/STTR process is the Commercialization Readiness Program or CRP which provides a strategically driven approach to directly link Air Force centers to AFRL technical points of contact (TPOCs). To establish better communication and identify needs with Air Force customers, the Air Force SBIR/STTR program office has established a network of Transition Agents or TAs. The TAs work closely with the organizations that they support and help them implement the entire SBIR or STTR process from topic generation to facilitating the transition of resulting technologies. Be sure to look at the AFRL produced videos in the Tools section of this tutorial to learn more about these organizations.</p>
      <br>
      <p>Let’s look at the types of technologies that are of interest to the Air Force. As one would expect the largest focus area is Air Platforms, followed by Sensors, Materials, and Space Platforms.  Other areas of interest include Human Systems, Information,  Weapons, Electronics, and Nuclear.</p>
      <br>
      <p>This figure shows the profile of small businesses that win awards from the Air Force SBIR/STTR programs. As you can see most of the companies are very small: Thirty-nine percent have between one and nine employees, while an additional 22% percent have between ten and twenty-four employees. Adding these two numbers together, we see that 61% are very small businesses. Looking at the other end of the spectrum only 14% of the awardees have over 100 employees with 12% having between 100 and 249 employees and 2% of the awardees having between 250 and 500 employees. Another interesting data point is that 25% of the awardees are first time winners – meaning that this was the first award they had won from any agency.</p>
      <br>
      <p>It’s recommended that small businesses that wish to develop a relationship with the Air Force stay current with changes taking place through periodically checking the Air Force SBIR/STTR website and reviewing solicitations. Another recommendation is to get to know the Air Force Small Business office.</p>
      <br>
      <iframe width="100%" height="390" src="https://www.youtube.com/embed/QZM0rhrQ9qs?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
      <br><br>
      <p>It takes considerable time, numerous rounds of funding, and many relationships in order to transition technology to the warfighter. From a funding perspective, it’s important to be aware of potential sources of funding that can help you continue the maturation of your technology beyond Phase II and into Phase III. Organizations such as the Air Force SBIR/STTR program office recognize this and take advantage of opportunities to provide additional funding for good firms that are diligent with developing needed technology. Air Force utilizes Phase II awards to provide up to $1.5M for prototypes and demonstrations typically completed in 18-24 months; uses Sequential Phase II awards and enhancements for technology testing and evaluation and has just begun to implement a Direct to Phase 2 pilot program to expedite the development of technologies that meet a critical need. Please consult course 1 on SBIR basics in order to learn more about some of the Phase II alternatives that are being piloted as a result of the most recent reauthorization.</p>
