                <br>
                <p>In 2015 the combined SBIR and STTR budget across all of the participating agencies was $2.5 billion dollars. Of that a little over half was provided to small businesses in the form of contracts; while the other half was provided as grants. What’s the significance of this? Why should you care if some agencies provide contracts and others provide grants? Contracts have a very specific deliverable; while grants are much more open-ended. Agencies that engage in basic research such as the Department of Health and Human Services tend to provide grants which provide considerable latitude to applicants in defining research problems of interest.</p>
                <br>
                <p>The Department of Health and Human Services or HHS actually provides both grants and contracts with the preponderance of its awards being made as grants. The National Institutes of Health or NIH is the largest granting institution participating in the SBIR/STTR program and is the largest component within HHS. Given the size of the NIH program which has roughly 90-95% of the HHS SBIR budget, you often hear people speak only about the NIH program.</p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/d9TWmsi1wsM?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>The mission of National Institutes of Health is very broad and includes the application of knowledge to enhance health, lengthen life, and reduce illness and disability. The National Institutes of Health include 27 Institutes and Centers, referred to as ICs. Twenty-three of these provide SBIR or STTR awards. However, there are four organizations that do not have the funding authority to participate in these programs. The SBIR Program Office is located in the Office of the Director. However, each participating IC also has its own  SBIR/STTR point of contact.</p>
                <br>
                <p>If we look at the budget of the NIH Institutes and Centers, you find tremendous variability in the size of their budgets. The institute with the largest budget is the National Cancer Institute or NCI, followed by the National Institute of Allergies and Infectious Diseases (NIAID), the National Heart, Lung, and Blood Institute (NHLBI), and the National Institute of General Medical Sciences (NIGMS).</p>
                <br>
                <p>NIH refers to its solicitations as Funding Opportunity Announcements or FOA. The NIH SBIR and STTR programs commonly use what is referred to as parent funding opportunity announcements, also known as Omnibus solicitations, which allow applicants to submit investigator-initiated projects for consideration by any of the NIH 24 Institutes and Centers (ICs), the Center for Disease Control (CDC), and the Food and Drug Administration (FDA). The Omnibus is released once a year and allows three occasions when an applicant may submit a proposal in response to a topic in the omnibus solicitation. This practice is unique to HHS as most agencies allow one opportunity to respond to a specific solicitation. In response to the FOA released in June, 2015, applicants may submit proposals on September 5, 2015; January 5th, 2016; and April 5th, 2016. Examples of some of the topic areas that you will find in the FOA include biodefense, biosensors, telehealth, proteomics, and imaging devices to name a few. Please note that it is recommended that you always contact the IC Program Manager before submitting an application. This is considered critical for success.</p>
                <br>
                <p>In addition to the Omnibus solicitation, the NIH Institutes and Centers also issue targeted funding opportunity announcements for certain research areas, as well as an annual NIH SBIR contract solicitation.</p>
                <br>
                <p>Phase I SBIR and STTR awards are typically for $150,000. However, the Reauthorization Act of 2011 allowed for a hard cap of $225,000 for Phase I and $1.5M for Phase II. As the development of medical technologies is known to be very expensive, proposers that can justify the need for more funds may apply for a waiver from SBA to go above this hard cap. In addition,  SBA recently has approved a specific list of  topics which are approved to exceed these caps. Another change brought about by the most recent reauthorization is the eligibility of small business concerns that are majority-owned by multiple venture capital operation companies, hedge funds, and private equity firms. Previously this was not allowed.</p>
                <br>
              </div>

