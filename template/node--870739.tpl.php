                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/cl5LhPrU8NY?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>What is the likelihood of winning a Phase I SBIR or STTR award from NIH? The table in the video shows the likelihood of winning a Phase I, Phase II, or Fast-Track award from NIH in 2014 and 2015. Fast-Track, by the way refers to submitting a Phase I and Phase II application concurrently, as a means of decreasing the delay between Phase I and Phase II awards. In looking at 2015, you see that NIH received 3,425 SBIR proposals and made phase I awards to 15% of the applicants or 514 projects. With respect to STTR Phase I applicants, 911 proposals were submitted and 16.4% of the applicants received an award. </p>
                <br>
                <p>The review process of SBIR and STTR applications is unique and involves initial review by the NIH Center for Scientific Review which assigns the application to an IC and IRG. After the IC staff prepares a funding plan for the IC director the application is then sent to the Advisory Council or Board which recommends approval. Please  be sure to consult the webinars that are referenced in the tools section of this tutorial in order to learn more about the review process.</p>
                <br>
                <p>NIH does many things in an effort to move technologies forward. A relatively new initiative is the Phase IIB program which is a sequential Phase II award. This allows a project to receive an additional $1 million a year for up to 3 years on a specific project. There is a Competing Renewal Application process for Phase IIB awards. NIH has also implemented most of the new initiatives allowed by the most recent reauthorization. This includes the ability to shift from an SBIR to an STTR award or from an STTR to and SBIR award when going from Phase I to Phase II. NIH has also started a Direct to Phase II pilot Program which allows companies that have done the work typically associated with Phase I with non-SBIR funding to start engagement in the program SBIR program with a Phase II award. Being a pilot program the Direct to Phase II awards are authorized through 2017. At that time, with the next reauthorization the decision will be made about the continuation of this program.</p>
                <br>
                <p>The newest initiative to be implemented by HHS is another pilot program called the Commercialization Readiness Pilot Program or CRP. A limited number of NIH Institutes and Centers and CDC are participating in this initiative. HHS issued two FOA in November, 2015 under the activity code SB1. Essentially the CRP is somewhere between Phase II and Phase III, but it is referred to by HHS, as a CRP award. A CRP applicant and awardee must have received an HHS SBIR or STTR Phase II or IIB award within the past three years. The due dates for the CRP submissions are the same as those associated with the Omnibus Funding Opportunity Announcement. The HHS CRP initiative is only available for SBIR awardees. However, there will not be a September 5th, 2017 due date because the authority expires September 30th, 2017. The CRP max that is authorized is $3 million over a three year period, but the participating Institutes and Centers vary greatly in the amount that each allows.</p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/f35d3cA43-k?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>HHS also has two technical assistance programs provided to HHS awardees. The Niche Assessment Program is offered by Foresight S&T to Phase I awardees. This program identifies other potential uses of the technology, determines the competitive advantage, and market entry strategies. For Phase II awardees another service provided by LARTA is called the Commercialization Accelerator Program or CAP. This program offers a menu of services including strategic/business planning, FDA requirements, technology valuation, manufacturing issues, and patent and licensing issues.</p>
                <br>
                <p>To learn more about the HHS SBIR and STTR programs be sure to consult the NIH SBIR/STTR program website and don’t hesitate to reach out to the various representatives of the SBIR and STTR program located at the Office of the Director or to the points of contact within the individual Institute, Centers, or components. The SBIR/STTR application infographic provides a good high level overview of the  NIH SBIR/STTR programs.</p>
