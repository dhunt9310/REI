                <p>Since the Small Business Innovation Research (SBIR) program began over 30 years ago, there have been many accommodations made to address different ways that eligible small business can meet the objectives of the SBIR program. Most of these variations are associated with Phase II. </p>
                <br>
                <p>As we introduce these variations, it is important to keep in mind that there are significant agency differences. Therefore, not all of the items discussed will apply in every situation. When in doubt, please contact the SBIR or STTR program manager for the agency of interest. The newest variations will be introduced first - these were brought about by the SBIR/STTR Reauthorization Act of 2011 and include Direct to Phase II awards, Sequential Phase II awards, Cross-agency and Cross-program awards, Open Phase II Competition, and Award Caps. </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/DZ_mkxUDmAg?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>So what is a Direct to Phase II? Under the SBIR/STTR Reauthorization Act of 2011, through Fiscal Year 2017, the National Institutes of Health (NIH), the Department of Defense and the Department of Education may issue a Phase II award to a small business concern that did not receive a Phase I award for research & development for the same project. Direct to Phase II awards are designed for small business concerns, also known as SBCs that have completed Phase I milestones using non-SBIR funds. </p>
                <br>
                <p>According to NIH “The legislative rationale for permitting the Direct-to Phase II awards is to allow a small business concern that has already built a technology prototype and tested its feasibility, that is, completed Phase I type R&D, to move directly into a Phase II type R&D that tests the functional viability of the prototype according to scientific methods and the potential for commercial development. The Direct to Phase II authority is NOT available to the STTR program, and within the Department of Health and Human Services IS available to NIH Institutes and Centers through specific solicitations for this Phase II Pilot program.” NIH suggests for example that if you are already performing clinical trials for a therapeutic, you may be a good candidate for a Direct to Phase II award. </p>
                <br>
                <p>The first Department of Defense (DoD) component to implement Direct to Phase II awards, also referred to as ‘DP2’, was the Defense Advanced Research Projects Agency (DARPA). DARPA has provided significant detail regarding the types of documentation that must be provided as part of the application process for DP2 proposals including technical reports, test data, prototype designs and models, as well as performance goals and results. The DP2 pilot program at DARPA is limited to SBIR applications and to specific topics. Keep in mind that Direct to Phase II is a pilot program scheduled to conclude at the end of FY 2017 pending any changes to future SBIR legislation. It is appropriate for firms that have completed Phase I milestones with non-SBIR funds. </p>
                <br>
                <div class="image">
                    <img src="../media/img/4_Slide2.png">
                </div>
                <br>
                <p>Let’s leave this topic and shift to the question of sequential Phase II awards. Recognizing that it takes a long time to mature a technology, the SBIR/STTR Reauthorization Act of 2011 allows a Phase II awardee to receive one additional sequential Phase II award to continue the work of an initial Phase II award. This additional sequential Phase II award has the same guideline amounts and limits as the initial Phase II award – that is a guideline amount of $1 million and a limit of $1.5 million. SBC’s that are interested in learning more about the application process for sequential Phase II’s should discuss this with their technical monitor or the SBIR/STTR program manager of the agency with which they are working.</p>

              </div>

              <!-- Page 2 -->
              <div class="page" id="two">
                <br>
                <p>Some variations that you will see are referred to as Phase IIA and Phase IIB awards. Again, please keep in mind that not every agency offers these awards and that guidelines will vary within Agencies and often between entities within an agency – so you should discuss these options with the agency from which you have SBIR or STTR funding. Within the Department of Energy for example a Phase II grantee may request additional financial support through a Phase IIA grant. A Phase IIA award allows the grantee to complete the R&D associated with initial prototype, product, or process development. By contrast a DOE Phase IIB award provides additional financial support for new R&D task that extend beyond the scope of the original Phase II grant. </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/MkP_qLV0Ayg?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>The National Cancer Institute’s (NCI) SBIR Program has developed an innovative funding opportunity called the SBIR Phase IIB Bridge Award to support the next stage of development for NIH-funded SBIR Phase II projects in the areas of cancer therapeutics, imaging technologies, interventional devices, diagnostics, and prognostics. The purpose of this award is to address the funding gap known as the "Valley of Death" between the end of the SBIR Phase II award and the subsequent round of financing needed to advance a product or service toward commercialization. To achieve this goal, the Bridge Award funding opportunity is specifically designed to incentivize partnerships between NIH's SBIR Phase II awardees and third-party investors and/or strategic partners. Both SBIR and STTR Phase II awardees from other Federal agencies are now welcome to apply. </p>
                <br>
                <p>Two additional capabilities were afforded by the 2011 Reauthorization referred to as Cross -program and Cross-agency awards. Cross-program awards enable a Phase I STTR award winner to receive a Phase II SBIR award or the reverse; while the concept of Cross agency awards enables any agency to fund an initiative in Phase II that was initially funded by another agency in Phase I. This can be particularly helpful when the agency that funded the Phase I award no longer has the funds to support a Phase II initiative. Be sure to consult with the SBIR program manager responsible for your Phase I award to determine how to take advantage of these opportunities. </p>
                <br>
                <div class="image">
                    <img src="../media/img/4_Slide3.png">
                </div>
                <br>
                <p>Another change brought about by the most recent reauthorization is Open Phase II competition. While most civilian agencies have always allowed a Phase I awardee to apply for a Phase II SBIR or STTR award, this was not previously the case with the Department of Defense. In the past, DoD Phase I awardees were invited to submit a Phase II proposal if the service or component had an interest in receiving a Phase II proposal from that firm. Now, all DoD Phase I awardees have the opportunity to submit a Phase II proposal to DoD without invitation but the mechanism for doing so varies by service and component. </p>
                <br>
                <p>Finally, across all agencies a ceiling or cap has been placed on all SBIR/STTR awards. An agency MAY provide awards capped at 150% of the base - the cap then is $225,000 for each Phase I award and $1.5 million for each Phase II award. Please note the use of the word “may”, as agencies have discretion regarding the size of their awards and may offer smaller awards so that the agency can increase the number of Phase I awards that it makes. In addition, SBA has approved a topics list, which allows budgets to exceed the hard caps as long as the project topics are on the list. HHS has topic waivers for hard caps.</p>
                <br>
                <p>Please keep in mind that although the percentage of funding available through the SBIR and STTR programs has been increasing, the funding for these additional Phase II opportunities comes out of the same pot of money. Each agency makes a decision about the size and number of awards that it can provide in Phase I, Phase II and as sequential Phase IIs. The agencies with the smaller budgets are usually not in a position to fund the Phase II opportunities discussed here, but often welcome Cross-agency awards as a means of bringing in new technologies to their programs that may meet urgent needs. It is the larger agencies that tend to implement more of these Phase II opportunities. However, keep in mind that competition for additional Phase II opportunities is significant, as the number of these awards is limited. Please be sure to review the guidelines provided by each agency which offers an SBIR and/or STTR program and discuss the wisdom of taking advantage of these new opportunities with your technical monitor or the SBIR or STTR program manager. </p>
