                <br>
                <p>
                  In this Tutorial we will look in greater detail at the differences between the Small Business Innovation Research (SBIR) and the Small Business Technology Transfer (STTR) programs. Both programs have three phases, and are focused on developing innovative solutions to pressing problems that can then be commercialized as new products and services or as improvements to existing ones. 
                </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/byGqfwVq1EM?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>          
                <p>
                  There are however two key differences between the SBIR and STTR programs. First, an STTR project requires the small business - which is always the applicant – to be teamed with a non-profit research institution, typically a university or Federal Laboratory. The second difference is that the STTR program is focused on the transfer of technology from the Research Institution, also referred to as the RI, to the small business and ultimately to the marketplace through a Phase 1-2-3 sequence. This second difference has been expanded over time to include situations where the innovation belongs to the small business, but the firm desires to include important resources from a nonprofit RI in the technology’s development. Research institutions typically have sophisticated laboratories and personnel with needed expertise.
                </p>
                <br>
                <p>
                  In addition to these two key differences, there are a number of other, more subtle differences between SBIR and STTR. One important difference is the primary employment of the project’s principal investigator. In an SBIR project, the PI must be primarily employed at the proposing small business, meaning that he or she cannot work full time elsewhere during the project period. With an STTR, the PI could be primarily employed at either the RI or the small business.  Another important difference between the two programs is that up to 60% of the research effort for an STTR project can be subcontracted, whereas there is a 33% limit on Phase I SBIR subcontracting. The SBIR limit increases to 50% in Phase II, but is still less than what is allowed in STTR. 
                </p>
                <br>
                <p>
                  There are several misunderstandings about what can be funded with an STTR. One misunderstanding is the belief that a university can apply for an STTR. This is incorrect because as the name “Small Business Technology Transfer” program implies - the applicant must be a small, for-profit business. Another misunderstanding is the belief that STTR is less focused on commercialization than SBIR. However, Congress and the agencies both intend STTR projects to lead to products and services in Phase III.  And while it was once true that a project that began as an STTR in Phase I had to remain as an STTR in Phase II, the 2011 Reauthorization permits a Phase I STTR award to morph into an SBIR in Phase II, and vice versa.
                </p>
                <br>
                <p>
                  Let’s now look at situations where it is best to apply for an SBIR award instead of an STTR.  Before we begin please keep in mind that 11 agencies participate in the SBIR program, while only five participate in STTR. So STTR is not always a consideration. In general, the SBIR program will be a better opportunity in the following situations:
                </p>
                <br>
                <div class="image">
                    <img src="../media/img/3_Slide2.png">
                </div>
                <br>
                <p>
                  <ul>
                    <li>First, when you submit an SBIR proposal you can include another company – large or small, as a subcontractor to enhance the capability of your team. You can also include a university or another type of research institution such as a Federally Funded Research and Development Center or FFRDC as a subcontractor. However, if you don’t plan to include a non-profit research entity in your project as a subcontractor, then STTR is not a consideration. </li>
                    <li>Another situation when SBIR is best - If you decide to subcontract to a Research Institution – but don’t need to subcontract more than 33% of a Phase I research effort to the RI, then SBIR is the most appropriate choice. </li>
                    <li>Here’s another situation where SBIR is best…. Sometimes potential investors do not like the idea of involving a non-profit in a project, whether it be because of perceptions about the “culture” of such participants or concerns about the non-profit’s claim to intellectual property rights on the project. In this case an SBIR would be the most appropriate choice. Please note that the reason for this concern is that if a small business participates in an STTR it is required that the small business and the not for profit entity establish an agreement which details the allocation of intellectual property.</li>
                    <li>A final reason to consider SBIR is if the personnel at the research institution would prefer to participate in the project as an independent consultant rather than as representatives of the university or Federal Laboratory.  A small firm hiring consultants does not qualify for STTR because there is no nonprofit RI acting as a subcontractor.  </li>
                  </ul>
                </p>
              </div>

              <!-- Page 2 -->
              <div class="page" id="two">
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/p06PnOc_FRw?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>  
                <p>
                  We’ve just outlined when it is best to consider SBIR –  Now let’s explore when it is best to consider STTR. Please keep in mind that only five agencies have an STTR program. Therefore, the Small Business Technology Transfer program will only be a consideration when seeking opportunities with the Department of Defense, The Department of Energy, the Department of Health and Human Services, the National Aeronautics and Space Administration, or the National Science Foundation. The STTR program would be the better choice in the following situations:
                </p>
                <br>
                <p>
                  <ul>
                    <li>Situation 1: In order to have the best team, you need to subcontract a large portion of the award.  SBIR Phase I projects constrain the use of subcontractors because no more than 33% of the research can be subcontracted; STTR is much more generous, allowing for up to 60% of the R&D to go to the nonprofit RI. The guidelines for STTR specifically state that the small business must perform 40% of the work on the project and the research institution must perform at least 30% of the R&D. This leaves an additional 30% that can be outsourced to either the research institution or another subcontractor.</li>
                    <li>Situation 2: The small business and the research institution view themselves as equal partners on the project.  Again, STTR is described as a “collaboration” between the two, suggesting the players are equals even though the award always goes to the small business and the firm must maintain overall control and has responsibility for the project.</li>
                    <li>The third situation - Sometimes the applicant may conclude that there is a greater probability of winning an award when teamed with an RI. </li>
                    <li>And finally, if a topic of interest is restricted to an STTR response then there is no opportunity to submit an SBIR proposal in response to it. </li>
                  </ul>
                </p>
                <br>
                <div class="image">
                    <img src="../media/img/3_Slide3.png">
                </div>
                <br>
                <p>
                  We’ve provided you with a general overview of when it is best to consider an SBIR or an STTR application. In the individual agency courses, other subtle variations will be presented. 
                </p>
