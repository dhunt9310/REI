              <div class="page" id="one">
                <br>
                <p>Apollo astronaut Frank Borman once said, "Exploration is the essence of the human spirit." At the National Aeronautics and Space Administration, better known as NASA, the human spirit gets a lot of exercise. For example, three SBIR companies supplied technology for the Phoenix Lander mission. The Phoenix mission landed in the Mars arctic region where the Mars Odyssey spacecraft previously identified the presence of water. A small business, Honeybee Robotics supplied the icy soil acquisition device; Yardney Technical Products provided lithium-ion batteries and Starsys Research now part of SpaceDev provided key elements of the wet chemistry laboratory.  Looking to the future, “the human soul starved for a challenge” will find opportunities on missions to explore deep space with the Space Launch System, with Orion, and many other missions.</p>
                <br>
                <p>NASA’s combined annual SBIR/STTR budget is between $160 and $190 million dollars a year. The topics and subtopics support the needs of the four NASA Mission Directorates (MD) – these are the Aeronautics Research; the Human Exploration and Operations; the Science; and the Space Technology Mission Directorates. The topics and subtopics found in the NASA SBIR and STTR solicitations are defined by the four mission directorates, in combination with the ten NASA centers.</p>
                <br>
                <p>NASA releases SBIR and STTR topics once a year usually in mid-November in two separate solicitations – one for SBIR and another for the STTR program. Phase I proposals are due in late January/early February. The maximum value of Phase I contracts for either SBIR or STTR awards is $125,000. The period of performance for Phase I SBIR awards is 6 months, while the STTR Phase I award period is 12 months. Phase II contracts are for up to $750,000 and have a maximum period of performance of 24 months. The Phase II dollar amount and the period of performance are identical for both SBIR and STTR awards. </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/yGCWlNfXvVE?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>In each solicitation there are a wide variety of topics and subtopics.  The accompanying figure, associated with the FY16 SBIR release, shows which topics are essentially the same, which are substantially changed, and which are new. SBIR and STTR topics are mapped to NASA’s Technology Area or TA roadmaps. The accompanying figure indicates that the most frequently occurring topics in the FY16 solicitation are mapped to Robotics and Autonomous Systems; Human Health, Life Support, and Habitation Systems; and Materials, Structures, Mechanical Systems and Manufacturing. Each topic in the SBIR/STTR solicitation involves a Lead center and one or more other centers, listed as participating centers.  All of the 10 Field Centers participate in the SBIR solicitation. Each center acts as a lead for some topics and as the participating center for others.</p>
                <br>
                <p>The accompanying graphic shows the progression of technology development from the time of an SBIR/STTR award to commercialization or infusion. NASA, by the way has always had a strong interest in dual–use applications of NASA-funded technologies. When a NASA funded project is successful in the commercial sector it is said to be commercialized, while NASA uses the term “infusion” to indicate the adoption of a technology by NASA in one or more of its Mission Directorates. </p>
                <br>
              </div>

              <!-- Page 2 -->
              <div class="page" id="two">
                <p>During Phase II, companies become eligible for either a Phase II-E or a Phase II-X award. Phase II-E or Enhancement awards are meant to encourage the advancement of innovations developed under Phase II. Firms must secure an external investor either from a non-SBIR/STTR source within NASA or from a source external to NASA. The SBIR/STTR contribution will be up to $125,000 or up to  $150,000 depending upon the solicitation year in which the original award was made. This award is a one to one match. The period of additional performance for a Phase II-E award is six to twelve months. </p>
                <br>
                <p>Phase II-X, is an eXpanded award intended to encourage transition of Phase II SBIR/STTR technologies into NASA programs and missions. NASA will provide a 2 to 1 match of up to $250,000 or up to $500,000 again depending upon the solicitation year in which the original award was made. This is a 2 to 1 match for non-SBIR/non-STTR NASA sources. </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/Ui1N8FmqXno?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>NASA also has a Commercialization Readiness Program or CRP. One of the unique features of this program relative to the others discussed is that the application must be submitted by a NASA civil servant. CRP provides a 1 to 1 match of SBIR/STTR funds to NON-SBIR/STTR funds. The award amount varies between $100K and $1.5M. The purpose of the CRP is to encourage engagement with one of the following four groups: (1) NASA programs and missions, (2) NASA prime contractors, (3) State and regional entities, and (4) Other networks.  </p>
                <br>
                <p>With this as background, let’s consider the likelihood of winning a NASA SBIR or STTR award. This figure shows the number of proposals received by NASA annually over a period of nine years.  Also depicted is the number of Phase I and Phase II awards made during that period. The range varies in recent years from 14-21%, meaning that of the proposals received, 14 to 21% will win a Phase I award, depending upon the available funding. </p>
                <br>
                <p>As Carl Sagan said “ Somewhere, something incredible is waiting to be known.” If you are interested in learning more about the “incredible”, be sure to explore the NASA SBIR/STTR website, as well as NASA’s Technology Area roadmaps. Videos related to the Space Launch System, Orion, and Mars exploration are listed under Tools within this tutorial.</p>
