              <div class="page" id="one">
                  <p>
                      In this tutorial we will introduce you to two important programs:  the Small Business Innovation Research Program, also referred to as SBIR and the Small Business Technology Transfer program, also referred to as STTR.  These two initiatives, the SBIR and STTR programs are sometimes referred to as the nation’s largest source of early stage/high risk funding for start-ups and small business.  To be eligible, the small business must be American-owned, organized as a for-profit entity, and have less than 500 employees. There are 11 Federal agencies that participate annually in the SBIR program and five that participate in the STTR program. 
                  </p>
                  <br>
                  <div class="image">
                      <img src="../media/img/Slide2.png">
                  </div>
                  <br>
                  <p>
                      These agencies designate R&amp;D topics and accept proposals. They fund innovative research that will meet the objectives of the agency. Each has a unique mission and should be viewed as distinct customers. In a subsequent course you will be introduced to the individual requirements of the Federal agencies that provide SBIR and/or STTR funding.
                  </p>
                  <br>
                  <iframe width="100%" height="390" src="https://www.youtube.com/embed/12Yl6X6wYxs?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                  <br><br>
                  <p>
                    The original charter of the SBIR program was to address four goals.
                    <ul>
                      <li>Stimulate technological innovation </li>
                      <li>Use small business to meet Federal R/R&amp;D needs </li>
                      <li>Foster and encourage participation by the socially and economically disadvantaged small businesses, and those that are 51 percent owned and controlled by women, in technological innovation </li>
                      <li>Increase private sector commercialization of innovations derived from Federal R/R&amp;D, thereby increasing competition, productivity, and economic growth </li>
                    </ul>
                  </p>
                  <br>
                  <p>
                    <strong>Since the SBIR program started in 1982, the charter has expanded to:</strong>
                    <ul>
                      <li>Require evaluation of commercial potential in Phase I and Phase II applications, and</li>
                      <li>Is viewed as seed capital for early stage R&amp;D with commercial potential 
                        <ul>
                          <li>The awards are comparable in size to angel investments in the private sector </li>
                          <li>and indicate the acceptance of greater risk in support of agency missions</li>
                        </ul>
                      </li>
                    </ul>
                  </p>
                  <br>   
              </div>

              <!-- Page 2 -->
              <div class="page" id="two">
                <p>
                  Both the SBIR and STTR programs have three Phases:  Following submission of proposals, agencies make awards based on small business qualification, degree of innovation, technical merit, and future market potential. Small businesses that receive awards then begin a three-phase program.
                  <ul>
                    <li>Phase I is the concept phase. It lasts six to nine months and supports exploration of the technical merit or feasibility of an idea or technology.</li>
                    <li>Phase II awards may last for up to 2 years and expand upon the Phase I results. During this time, the R&D work is performed. </li>
                    <li>Phase III is the period during which Phase II innovation moves from the laboratory into the marketplace. No SBIR funds support this phase. The small business must find funding in the private sector or secure it from other non-SBIR Federal agencies that provide funding for continued development.</li>
                  </ul>
                </p>
                <br>
                <div class="image">
                    <img src="../media/img/Slide4.jpg">
                </div>
                <br>
                <p>
                    The Small Business Technology Transfer program or STTR came later and was modeled after the SBIR program. Its goal however, is to facilitate the transfer of technology developed by a research institution through the entrepreneurship of a small business concern. Research institutions include universities and Federally Funded Research and Development Centers, also referred to as FFRDC. It is important to keep in mind that the applicant for an STTR award is always the small business.
                </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/AlGexFVDur4?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>
                    To succeed in either the SBIR and STTR programs there are a number of prerequisites:
                    <ul>
                      <li>First, you must have a burning desire to conduct innovative research and development </li>
                      <li>AND Second, a desire to develop a viable product that you want to bring to the marketplace-Just wanting to do research is insufficient – you must want to see that research commercialized or turned into a useful product that you and your firm will promote either directly or through a variety of commercialization strategies. </li>
                    </ul>
                </p>
                <br>
                <p>
                    If you can match your interests and talents with the specific needs of a Federal agency you may be able to secure funding to conduct research and development with no strings attached. By applying for and winning an SBIR or STTR award, you will receive funding to conduct the R&D, retain the rights to the intellectual property and have the opportunity to obtain additional funding to continue the development of the technology.  The SBIR and STTR programs provide a unique opportunity for scientists and engineers to develop and grow a small business.
                </p>
                <br>
                <p>
                    But don’t get ahead of yourself – Often, when people first hear about the SBIR program we hear the following question – if I have already developed a solution can I apply? No – the SBIR and STTR programs fund innovative research and development – the purpose is not to retroactively pay a company for development that they may have already accomplished. However, it is assumed that you have an area of expertise and perhaps related work.
                </p>
                <br>
                <p>
                    The emphasis on innovation is important in both programs and implies that you are proposing a novel approach to pressing problems or needs identified by an agency.  The approach proposed should be unproven and involve an element of technical risk. You must clearly identify the innovation in your proposal. Do not leave the recognition of your innovation to the imagination of the reviewers. You must be explicit.
                </p>
                <br>
                <p>
                    As you can see small businesses participating in the SBIR or STTR program receive many benefits:  you receive funding with no strings attached – you do not give up equity in your company, you do not give up rights to the intellectual property, you do not return the funding – as this is not a loan. What is expected is good research that will lead to a commercial product and that will benefit the nation. 
                </p>
	            </div>


