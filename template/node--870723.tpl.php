                <br>
                <p>The Small Business Innovation Research and the Small Business Technology Transfer programs are both considered small business set-asides and contain sunset provisions. Funds are restricted or set aside exclusively for use by small businesses in both of these initiatives. The Small Business Administration establishes size standards on an industry-by-industry basis using the North American Industry Classification or NAICS codes. </p>
                <br>
                <p>The sunset provision is important and provides that the law that brought these programs into effect will cease after a specific date unless further legislative action is taken to extend the law. For this reason, since the SBIR program first began in 1982, it has periodically been reauthorized.</p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/vhYwKsDtlb4?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <br>
                <p>Before looking at the history of reauthorization let’s review why and how these programs began. In the 1970’s there was much concern about the US loosing its competitiveness in an era of increasing globalization. A growing body of evidence indicated that small businesses were playing an ever increasing role in innovation and job creation. It was during this period that the paths of a number of key players converged. Roland Tibbetts, long recognized as “the father of the SBIR program” was appointed as the Senior Program officer at NSF in 1972. Having held various positions in both large and small business, Tibbetts understood the importance of small advanced tech firms and recognized the opposition that they faced from other parties pursuing Federal R&D funding. At the same time, Senator Edward Kennedy, then Chairman of the National Science Foundation Subcommittee on the Senate Labor and Public Welfare committee, continued to increase the budget directed to small business.  What was born from this synergy was the precursor to the agency wide SBIR program, which began at NSF in 1977 as NSF SBIR.</p>
                <br>
                <div class="image">
                    <img src="../media/img/5_Slide2.png">
                </div>
                <br>
                <br>
                <p>In 1982, through the Small Business Innovation Development Act, the Small Business Innovation Research (SBIR) program was created and implemented across multiple agencies.. It has continued as the nation’s largest innovation program. SBIR offers competitive awards to stimulate technological innovation among small private-sector businesses while providing government agencies with new solutions that meet their diverse needs. </p>
                <br>
              </div>

              <!-- Page 2 -->
              <div class="page" id="two">
                <br>
                <p>Some important dates to consider:
                  <ul>
                    <li>The SBIR program was reauthorized in 1992 with the passage of the Small Business Research and Development Enhancement Act of 1992. At that time, concern continued about the US economy’s capacity to commercialize inventions. As a result, legislative language was added which highlighted commercial potential as a criterion for awarding SBIR grants.</li>
                    <li>Then again in 2000, the program was reauthorized by the Small Business Innovation Research Program Reauthorization Act of 2000 until 2009.</li>
                    <li>Due to a series of Continuing Resolutions the SBIR program was not reauthorized again until December 2011 when it was reauthorized by the 2012 Defense Authorization Act. The current SBIR program will continue until FY2017 when further legislative action is required to continue the program.</li>
                  </ul>
                </p>
                <br>
                <p>The Small Business Technology Transfer or STTR program began later and was initialized in 1992 by the Small Business Technology Transfer Act of 1992. It too contains a sunset provision and has been reauthorized by the Small Business Reauthorization Act of 1992; the Small Business Reauthorization Act of 1997, the Small Business Technology Transfer Program Reauthorization Act of 2001 and the SBIR/STTR Reauthorization Act of 2011. </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/0l4Zuv0hpPo?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <br>
                <p>Once a program has been reauthorized, the Small Business Administration has the statutory obligation to provide guidance to the participating Federal agencies for the general operation of the SBIR and STTR programs. This guidance is referred to as the Policy Directive. It is prepared by SBA and is subject to public comment before it is finalized. Federal agencies participating in the SBIR and STTR programs are obligated to follow the guidance provided by the Policy Directive. Each agency is required to review its rules, policies, and guidance to ensure consistency with the relevant policy directive and make any necessary changes in accordance with each agency’s normal procedures. The most recent policy directives for both the SBIR and STTR programs were published on February 24, 2014.</p>
                <br>
                <p>Though dynamically different, both programs share a common goal - to foster innovation among domestic small businesses engaged in Federal research and development activities. As such, both programs harness the entrepreneurial spirit for which the United Sates is recognized and will continue to evolve over time.  </p>
                <br>
