                <br>
                <p>
                  The Department of Defense (DoD) is often referred to as a mission agency. This implies that DoD is an intended customer for the technology that it funds through the SBIR or STTR program. However, this statement does not imply that DoD will purchase the final product directly from a small business, nor that any purchase will occur, as DoD needs frequently change and R&D may be unsuccessful or the solution may be too expensive. Most commonly an SBIR or STTR awardee working with the Department of Defense will develop a component of a much larger system, for example a component of an aircraft, a submarine, or a tank that the government will ultimately purchase from a large defense prime contractor. Most DoD SBIR/STTR awardees will therefore need to develop a relationship with a Lead System Integrator (LSI) or with one of their 1st or 2nd tier suppliers.
                </p>
                <br>
                <p>
                  In describing how the Department of Defense makes purchases, reference is often made to the Defense Acquisition System. The purpose of this tutorial is to provide you with a very brief introduction to the acquisition process, as insight into this will help you understand the Department of Defense and the unique relationships that are important for you to develop. This tutorial will also introduce you to many acronyms that are important to understand when working with DoD.
                </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/KNfzTz87z4I?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>
                  The term Acquisition implies much more than just the purchase of an item or service. The acquisition process encompasses the design, engineering, construction, testing, deployment, sustainment, and disposal of weapons or related items purchased from a contractor. There are three steps that a weapon system must go through before it can be deployed. These steps are identifying a need, establishing a budget, and acquiring the system. These processes are formally referred to as
                <br>
                <div class="image">
                    <img src="../media/img/15_Slide2.png">
                </div>
                <br>
                  <ol>
                    <li>The Joint Capabilities Integration and Development System (JCIDS) which is the process used for identifying warfighter requirements,</li>
                    <br>
                    <li>The Planning, Programming, Budgeting, and Execution System (PPBE) used for allocating resources and budgeting, and</li>
                    <br>
                    <li>Finally, The Defense Acquisition System (DAS) for developing and/or buying an item</li>
                  </ol>
                </p>
                <br>
                <p>
                  From a practical perspective the interaction of these three processes is challenging. The JCIDS system is needs based -  and warfighter needs can occur at any time. The PPBE process for budgeting is on a fixed schedule occurring annually and covering multiple planning years. The Defense acquisition process is event based and divided into phases, milestones, and reviews. These three processes must work together, as an acquisition program won’t begin unless there are identified warfighter needs, and can’t start until there is money to support the initiative.
                </p>
                <br>
                <p>
                  Our focus in this tutorial is to understand the Defense Acquisition Process.
                </p>
                <br>
                <div class="image">
                    <img src="../media/img/15_Slide3.png">
                </div>
                <br>
                <p>
                  There are 5 Phases identified in the accompanying Figure. Reading from left to right, the first phase is the Materiel Solution Analysis phase, followed by Technology Maturation and Risk Reduction, the Engineering and Manufacturing phase, Production and Deployment, and finally Operations and Support.  Keep in mind that these terms have very specific meanings when used by the Department of Defense and that the process describes how DoD as a whole develops and acquires needed technology. For a small business beginning to work with the Department of Defense, you need to think about where your project fits into this process.
                </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/Hlcjug2kgdU?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>
                  Let’s look at each phase a little more deeply. The purpose of the Materiel Solution Analysis or MSA phase is to select the most promising technology that can meet a user need. To enter this phase there must be an Initial Capabilities Document (ICD) based on interaction with the user community during the JCIDS process. A major activity during Materiel Solution Analysis is to provide study guidance for preparing an Analysis of Alternatives (AoA) and to complete the AOA so that the best solution can be selected, after a thorough review of alternatives. Another major activity during this period is to establish a Program Manager or Program Management Office for this initiative. Concurrently interaction continues with the user community that  develops a draft Capability Development Document or CDD.  Work also continues on the budget or PPBE process so that funding can be put in place for Milestone A. During the Material Solution Analysis phase, the acquisition community is working towards making a confident decision that will enable the organization to enter the next phase – Technology Maturation and Risk Reduction. Keep in mind that these are the activities with which the Department of Defense is engaged. This information is not shared with the small business awardees – this is what is going on within your customer organization that is responsible for the acquisition of major Defense platforms. Your technology is in all probability associated with one or more of these major platforms. However, in the course of  working on your contract you interact with a DoD technical monitor who may be positioned at a distance from where these decisions take place - both in terms of rank and physical location . 
                </p>
                <br>
              </div>

              <!-- Page 2 -->
              <div class="page" id="two">
                <p>
                  The second phase in the acquisition process is Technology Maturation and Risk Reduction when the best solution brought forward from the Material Solution Analysis  begins the maturation process. In order to enter this Phase Milestone A must have been achieved. Milestones are represented in this figure by triangles placed at the top of the diagram.  To enter Milestone A the program must have an approved Initial Capabilities Document.  An Analysis of Alternatives must have been conducted and an initial Life-Cycle Cost Estimate for the program must have been submitted. During the Technology Maturation and Risk reduction phase there is a great deal to be accomplished - competitive prototyping is conducted as well as cost performance trade analyses. An RFP is released and a Preliminary Design Review or PDR is conducted, and source selection begins. Interaction with the users continues – apprising them of what can be afforded. This is captured in the evolving Capability Development Document which at this point specifies what can be done at a certain cost to provide a technically feasible solution that addresses the identified needs. The CDD must be validated by the user. A major milestone in this process is a DRFPRD which gives the organization the right to release a request for proposals (RFP) for competitive bids and which clearly articulates the requirements. PPBE activities continue during this period.
                </p>
                <br>
                <p>
                  Another reminder to small businesses listening to this tutorial, please keep in mind that the process we are describing is what the Department of Defense is implementing with respect to major defense platforms.  Much of this information is not publicly available and may indeed be classified.  The implication is that just as you would not be privvy to strategic planning taking place within a Fortune 500 company with which you may be working, the same is true with the Defense Acquisition programs. For this reason, a number of the DoD SBIR program offices have developed a variety of methods to assist small businesses make appropriate connections within the Department of Defense and with large Defense prime contractors.
                </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/_xSqYk9q6vw?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>
                  The next phase is the Engineering and Manufacturing Development phase. It’s at this point,  that program initiation typically begins, that is when a program formally enters the acquisition process . At program initiation, a program must be fully funded across the Future Years Defense Program (FYPD) as a result of the Program Objectives Memorandum (POM) process.  During the Engineering and Manufacturing Development phase the final “build-to” design is put in place which in turn enables a critical design review. At this point one must also demonstrate interoperability and supportability so that the initiative can move on to Milestone C. The Capability Design Document continues to evolve and leads to the development of the Capability Production Document (CPD) which  must be approved and signed before going on to Milestone C.
                </p>
                <br>
                <p>
                  The fourth phase is Production and Deployment.  Here the primary task is to start production. This begins with Low Rate Production (LRIP). During LRIP 10% of the production buy can be conducted. Other Major activities include Initial Operational Test and Evaluation during which time the articles resulted from LRIP are tested. If all goes well then you are ready to begin Full Rate Production which may take years to complete. While all this is happening, support for the systems that have been fielded must occur – which is why there is an apparent overlap between these phase. Initial Operational Capability refers to the deployment of a units worth of equipment; while Full Operational Capability is achieved when all of that equipment has been fielded.
                </p>
                <br>
                <p>
                  Operations and support is the final and longest phase. It is also the most expensive. To enter this phase you must have an approved Capability Production Document(CPD), an approved Life Cycle  Sustainment Plan (LCSP) and  a successful Full Rate Production system (FRP). Major activities are supporting operational units for full system life and dispose of the system at the end of life.
                </p>
                <br>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/Pj2LHXIjmpo?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>
                  Well why do you, a small business working with the Department of Defense need to be concerned with the DoD acquisition process? DoD is a mission agency – meaning the Department of Defense is a customer for technology that will benefit the warfighter. The acquisition process that we have reviewed describes how DoD makes decisions about the development and purchase of major defense platforms. Your research is in all likelihood associated with one or more of these platforms – but how do you gain visibility. How will DoD know if you have something that is of value – as you are not part of that structure. Where does your technology fit into this process? Does it fit in at the Materiel Solution Analysis phase or can it be inserted later in the acquisition process? Where does funding come from and with which large defense prime contractors do you need to develop a relationship? Although these are not items that you are expected to consider at the outset. To be successful in Phase III, this becomes important. In reviewing the component overviews, pay attention to the programs that the components have to assist with this. 
                </p>
                <br>
              </div>

