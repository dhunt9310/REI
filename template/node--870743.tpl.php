              <div class="page" id="one">
                <p>The SBIR and STTR topics released by an Agency are aligned with both its mission and appropriations. The U.S. Department of Energy’s mission is “to ensure America’s security and prosperity by addressing its energy, environmental and nuclear challenges through transformative science and technology solutions”. To accomplish this mission DOE has identified three goals. Aligned with each of these are Program Offices that participate in the DOE SBIR and STTR programs.  </p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/MriIKOpG4DM?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>Goal 1 addresses clean energy. The intent is to “catalyze the timely, material, and efficient transformation of the nation’s energy system and secure U.S. leadership in clean energy.” The four Program Offices that implement this goal and release SBIR and STTR topics include the program offices of:
                  <ul>
                    <li>Electricity Delivery and Energy Reliability</li>
                    <li>Energy Efficiency and Renewable Energy</li>
                    <li>Fossil Energy, and</li>
                    <li>Nuclear Energy</li>
                  </ul>
                <p>
                <br>
                <p>The second goal is to “maintain a vibrant U.S. effort in science and engineering as a cornerstone of US economic prosperity with clear leadership in strategic areas.” The six Program Offices which facilitate this goal include: 
                  <ul>
                    <li>Advanced Scientific Computing Research</li>
                    <li>Basic Energy Sciences</li>
                    <li>Biological and Environmental Research</li>
                    <li>Fusion Energy Sciences</li>
                    <li>High Energy Physics, and</li>
                    <li>Nuclear Physics</li>
                  </ul>
                </p>
                <br>
                <p>The final goal associated with DOE’s mission is “to enhance nuclear security through defense, nonproliferation, and environmental efforts.” Two program offices associated with this goal also provide topics for DOE’s SBIR and STTR programs. These are the Program Offices for:
                  <ul>
                    <li>Defense Nuclear Nonproliferation and</li>
                    <li>Environmental Management</li>
                  </ul>
                </p>
                <br>
                <p>The Department of Energy releases two Funding Opportunity Announcements or FOA’s each fiscal year. The FOA, which you may think of as a solicitation, requests SBIR or STTR application submissions from small business. The topics are released annually in July and October. DOE is unique in that it releases its topics a month before the FOA is released. Most other agencies include their topics within their solicitation. However, DOE has found it beneficial to release its topics before the FOA is released. Webinars by the topic authors are provided shortly thereafter providing more guidance to potential applicants. Within three weeks of a FOA’s release, DOE requests a Letter of Intent or LOI from potential applicants. This is a unique practice of the Department of Energy which enables the Agency to secure appropriate reviewers and advise potential applicants if their concepts are non-responsive to the topic.  If a potential applicant does not submit an LOI, the applicant will not be allowed to submit a proposal, which DOE refers to as an application.</p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/ywvRTYEqtgA?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>The accompanying graphic shows how the SBIR/STTR program office interacts with the DOE Program Offices and the DOE Chicago Office. The DOE SBIR/STTR program office develop the FOA’s; administers the review and selection process; ensures compliance with SBIR/STTR legislation; and conducts outreach. However, it is the various program offices that we just identified which identify topics, identify the reviewers used in the scientific peer review process, recommend the awardees and manage the grants. The DOE Chicago office negotiates the contracts, issues new and continuation awards, and is responsible for grant closeout at the project’s conclusion.</p>
                <br>
                <p>Let’s take a quick look at some of the topics which DOE releases in each of its two Funding Opportunity Announcements. The first release is in July and only four of the program offices participate in this release: 
                  <ul>
                    <li>Advanced Scientific Computing Research</li>
                    <li>Basic Energy Sciences</li>
                    <li>Biological and Environmental Research and</li>
                    <li>Nuclear Physics</li>
                  </ul>
                </p>
              </div>

              <!-- Page 2 -->
              <div class="page" id="two">
                <br>
                <p>The types of topics you will find here include high performance computing and networking; modeling and simulation, advanced detectors and accelerator technology; instrumentation; atmospheric measurement technology; genomic sciences and related biotechnologies; as well as nuclear instrumentation and detection.</p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/dF0jm00VDyc?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>The second topic release occurs at the end of October. The clean energy topics participate in this second release. The Office of Energy Efficiency and Renewable Energy, also known as EERE, is one of the largest DOE programs and includes research on renewable energy sources such as solar, water, bioenergy and wind power. The Nuclear Energy area addresses primarily materials and subsystems; while the fossil energy topics relate to oil, gas, and coal and look to reduce carbon emissions. Four other Program Offices that fall under the Office of Science also participate in Release 2. The Office of Science by the way funds about two-thirds of all the SBIR/STTR topics. The Office of Defense Nuclear Nonproliferation also participates in the second FOA. DOE develops nuclear weapons grade material for the Department of Defense and is responsible for assuring that materials are secure and not moving around. Monitoring movement and possible detonations are important activities of this office. </p>
                <br>
                <p>DOE’s Phase I awards are either 150 or $225K and last 9 months to determine feasibility. Phase II awards are for either 1 or $1.5M and last for 24 months. DOE also makes sequential Phase II awards and differentiates between Phase IIA and IIB awards. Phase IIA to support new R&D tasks and activities aligned with the original scope of work; while IIB funds are to support R&D tasks and activities that extend beyond the scope of the original Phase II grant. </p>
                <br>
                <p>Another unique feature of the DOE program is that rather than having separate solicitations and topics for SBIR or STTR, any DOE topic may be submitted as either an SBIR or STTR application. In some instances proposals may be submitted as both. DOE also has a Fast-Track program where both Phase I and Phase II applications are submitted at the same time which avoids a funding gap between Phase I and Phase II.</p>
                <br>
                <iframe width="100%" height="390" src="https://www.youtube.com/embed/Sbxmd3-dt60?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
                <br><br>
                <p>If we look at the Application and Award statistics using FY15 as a prototypical year, you see that DOE received 1,556 applications and funded 208 Phase I projects. Five percent of the applications were declined without review, because the applicant did not follow the guidelines provided; 61% were reviewed but were not recommended for funding. Eighteen percent of the applications received awards, while another 16% were recommended but not awarded. This situation occurs when there are more quality proposals than there is money to fund them. </p>
                <br>
                <p>Looking at first time applicants and winners of DOE Phase I awards – you can see that there has been an upward trend since 2009. In 2014, 21% of the applicants were first time DOE applicants and 33% were first time awardees of a Department of Energy Phase I grant. In the proposal evaluation process, the criteria used in evaluating Phase I and Phase II proposals are weighted in the following fashion: 33% of the evaluation is based on the Strength of the Scientific or Technical Approach. DOE is looking for high risk, breakthrough technologies. One shouldn’t avoid risk. The applicant should be sure to demonstrate a clear knowledge of the current state of the art and articulate clearly how this approach will lead to a breakthrough.  Another 33% of the evaluation reflects the perceived ability of the Principal Investigator, other key staff and consultants to carry out the project in a cost effective manner. The final 33% measures impact, that is the likelihood that the proposed work will lead to a marketable product; have economic benefit; and attract further development funding from other sources once the SBIR or STTR award concludes.</p>
                <br>
                <p>To learn more about the Department of Energy’s SBIR/STTR programs please consult the Agency’s website and listen to the tutorial in Course 4 regarding the DOE Funding Opportunity Announcements.</p>
